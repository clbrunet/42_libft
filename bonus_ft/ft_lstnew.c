#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	if (!(new = malloc(sizeof(t_list))))
		return ((t_list *)NULL);
	new->content = content;
	new->next = (t_list *)NULL;
	return (new);
}
